    private void jEdpTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jEdpTestActionPerformed
        try {
            // Verbindung testen zum Server via edp
             EDPSession session = EDPFactory.createEDPSession(3.31);
             String passwd = new String(jPasswordField.getPassword());
             Integer edpport = Integer.parseInt(jPortField.getText());
             //Session aufbauen
             session.beginSession(jHostField.getText(), edpport, jMandantField.getText(), passwd, "JEDP_0001");
             session.endSession();
             JOptionPane.showMessageDialog(this, "Verbindungsaufnahme erfolgreich");
        } catch (CantBeginSessionException ex) {
            Logger.getLogger(Einstellungen.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Verbindungsaufnahme gescheitert\n\n" + ex, "EDP Fehler", JOptionPane.ERROR_MESSAGE);
        }          
    }//GEN-LAST:event_jEdpTestActionPerformed

